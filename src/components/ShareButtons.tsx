import { useState } from "react";
import { Share2, Facebook, Link, Check, X } from "lucide-react";
import { Button } from "@/components/ui/button";
import {
  Popover,
  PopoverContent,
  PopoverTrigger,
} from "@/components/ui/popover";
import { toast } from "sonner";

interface ShareButtonsProps {
  title: string;
  url?: string;
  listingId?: string;
}

const ShareButtons = ({ title, url, listingId }: ShareButtonsProps) => {
  const [copied, setCopied] = useState(false);
  const [open, setOpen] = useState(false);
  
  const canonicalUrl = url || window.location.href;

  // Sociala medier läser OG-taggar från server-renderad HTML.
  // Eftersom /listing/:id är en SPA-route behöver vi en "förhandsvisningslänk".
  const previewUrl = listingId
    ? `${import.meta.env.VITE_SUPABASE_URL}/functions/v1/og-image?id=${listingId}`
    : canonicalUrl;

  // Använd previewUrl för delning så att titel + bild blir rätt.
  const shareUrl = previewUrl;
  const encodedUrl = encodeURIComponent(shareUrl);
  const encodedTitle = encodeURIComponent(title);

  const handleCopyLink = async () => {
    try {
      await navigator.clipboard.writeText(previewUrl);
      setCopied(true);
      toast.success("Förhandsvisningslänk kopierad!");
      setTimeout(() => setCopied(false), 2000);
    } catch (err) {
      toast.error("Kunde inte kopiera länken");
    }
  };

  const handleCopyPlainLink = async () => {
    try {
      await navigator.clipboard.writeText(canonicalUrl);
      toast.success("Vanlig länk kopierad!");
    } catch {
      toast.error("Kunde inte kopiera länken");
    }
  };

  const shareToFacebook = () => {
    const newWindow = window.open(
      `https://www.facebook.com/sharer/sharer.php?u=${encodedUrl}`,
      "_blank",
      "noopener,noreferrer,width=600,height=400"
    );
    if (newWindow) newWindow.opener = null;
    setOpen(false);
  };

  const shareToX = () => {
    const newWindow = window.open(
      `https://twitter.com/intent/tweet?url=${encodedUrl}&text=${encodedTitle}`,
      "_blank",
      "noopener,noreferrer,width=600,height=400"
    );
    if (newWindow) newWindow.opener = null;
    setOpen(false);
  };

  const shareToWhatsApp = () => {
    const newWindow = window.open(
      `https://wa.me/?text=${encodedTitle}%20${encodedUrl}`,
      "_blank",
      "noopener,noreferrer"
    );
    if (newWindow) newWindow.opener = null;
    setOpen(false);
  };

  const shareToMessenger = () => {
    const newWindow = window.open(
      `https://www.facebook.com/dialog/send?link=${encodedUrl}&app_id=966242223397117&redirect_uri=${encodedUrl}`,
      "_blank",
      "noopener,noreferrer,width=600,height=400"
    );
    if (newWindow) newWindow.opener = null;
    setOpen(false);
  };

  const handleNativeShare = async () => {
    if (navigator.share) {
      try {
        await navigator.share({
          title: title,
          url: shareUrl,
        });
        setOpen(false);
      } catch (err) {
        // User cancelled or error
      }
    }
  };

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        <Button variant="outline" size="sm" className="gap-2">
          <Share2 className="w-4 h-4" />
          Dela
        </Button>
      </PopoverTrigger>
      <PopoverContent className="w-56 p-2" align="end">
        <div className="grid gap-1">
          {/* Native share (mobile) */}
          {typeof navigator !== 'undefined' && navigator.share && (
            <button
              onClick={handleNativeShare}
              className="flex items-center gap-3 w-full px-3 py-2 text-sm rounded-md hover:bg-accent transition-colors text-left"
            >
              <Share2 className="w-4 h-4 text-primary" />
              Dela via...
            </button>
          )}
          
          {/* Facebook */}
          <button
            onClick={shareToFacebook}
            className="flex items-center gap-3 w-full px-3 py-2 text-sm rounded-md hover:bg-accent transition-colors text-left"
          >
            <Facebook className="w-4 h-4 text-[#1877F2]" />
            Facebook
          </button>

          {/* X (Twitter) */}
          <button
            onClick={shareToX}
            className="flex items-center gap-3 w-full px-3 py-2 text-sm rounded-md hover:bg-accent transition-colors text-left"
          >
            <X className="w-4 h-4" />
            X (Twitter)
          </button>

          {/* WhatsApp */}
          <button
            onClick={shareToWhatsApp}
            className="flex items-center gap-3 w-full px-3 py-2 text-sm rounded-md hover:bg-accent transition-colors text-left"
          >
            <svg className="w-4 h-4 text-[#25D366]" viewBox="0 0 24 24" fill="currentColor">
              <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413z"/>
            </svg>
            WhatsApp
          </button>

          {/* Messenger */}
          <button
            onClick={shareToMessenger}
            className="flex items-center gap-3 w-full px-3 py-2 text-sm rounded-md hover:bg-accent transition-colors text-left"
          >
            <svg className="w-4 h-4 text-[#0084FF]" viewBox="0 0 24 24" fill="currentColor">
              <path d="M12 0C5.373 0 0 4.974 0 11.111c0 3.498 1.744 6.614 4.469 8.654V24l4.088-2.242c1.092.3 2.246.464 3.443.464 6.627 0 12-4.975 12-11.111S18.627 0 12 0zm1.191 14.963l-3.055-3.26-5.963 3.26L10.732 8l3.131 3.259L19.752 8l-6.561 6.963z"/>
            </svg>
            Messenger
          </button>

          <div className="my-1 border-t border-border" />

          {/* Copy Link (preview) */}
          <button
            onClick={handleCopyLink}
            className="flex items-center gap-3 w-full px-3 py-2 text-sm rounded-md hover:bg-accent transition-colors text-left"
          >
            {copied ? (
              <>
                <Check className="w-4 h-4 text-primary" />
                Kopierad!
              </>
            ) : (
              <>
                <Link className="w-4 h-4" />
                Kopiera länk (förhandsvisning)
              </>
            )}
          </button>

          {/* Copy regular link */}
          <button
            onClick={handleCopyPlainLink}
            className="flex items-center gap-3 w-full px-3 py-2 text-sm rounded-md hover:bg-accent transition-colors text-left"
          >
            <Link className="w-4 h-4" />
            Kopiera vanlig länk
          </button>
        </div>
      </PopoverContent>
    </Popover>
  );
};

export default ShareButtons;